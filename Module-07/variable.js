//Var is a global scope variable. We don't use it in modern javascript
var userName = "mohim"
console.log(userName)

//let is a block scope variable . We can reassign value in this variable.
let age=25
age = 26
console.log(age)


//const is a blocked scope variable . But we can't re-assign value.
const maritalStatus = "Married"
// maritalStatus = "Unmarried"
console.log(maritalStatus)

